"""Practicing the definition of the number of shown decimals using a printout formatting function. Noting that the
value and the printing specificity of a number are two different things. The approximate value of pi is 3 or,
if we want to get specific, 3.1416 """


def main():
    PI = 3.14159265358979323846
    print(f"The approximate value of pi is {PI:.0f} or, if we want to get specific, {PI:.4f}")


if __name__ == "__main__":
    main()
